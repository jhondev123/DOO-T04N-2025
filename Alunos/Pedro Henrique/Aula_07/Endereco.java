package gerenciamento_empresa;

public class Endereco {
	String rua, numero, bairro, cidade, uf;

    public Endereco(String rua, String numero, String bairro, String cidade, String uf) {
        this.rua = rua;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
        this.uf = uf;
    }
}
